# rice
# [2,1,6,8,4,9,0,20,12,35] n+n-1+n-2+.... O(n**2)
#                    ^
# 33,34,29,27,31,26,35,15,23 = 35

# O(n)

# [2,1,6,8,4,9,0,20,12,35] O(n)
#              i       j

# l=[2,1,6,8,4,9,0,20,12,35]
l=[3,5,9,12,1,0,15,2,4,50,20]
i=0
j=i+1
maxval=0
while(j<len(l)):
    if(l[i]>=l[j]):
        i=j
        j=i+1
    else:
        if(maxval<=(l[j]-l[i])):
            maxval=l[j]-l[i]
        j=j+1

print(maxval)
